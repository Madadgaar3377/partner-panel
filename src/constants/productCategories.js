// Helper function to get grouped categories
export const getGroupedCategories = () => {
    const grouped = {};
    PRODUCT_CATEGORIES.forEach(cat => {
        if (!grouped[cat.group]) {
            grouped[cat.group] = [];
        }
        grouped[cat.group].push(cat);
    });
    return grouped;
};
